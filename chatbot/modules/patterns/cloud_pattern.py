    reduce_mfa: int = 20
    reduce_least_privilege: int = 15
    reduce_privileged_access_management: int = 15
    reduce_iam_audit: int = 10
    reduce_encryption: int = 15
    reduce_bucket_policy: int = 20
    reduce_dlp: int = 10
    reduce_waf: int = 15
    reduce_api_gateway_auth: int = 20
    reduce_network_segmentation: int = 20
    reduce_cloudtrail: int = 20
