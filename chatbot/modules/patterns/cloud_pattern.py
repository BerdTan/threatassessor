"""
Cloud Infrastructure Threat Pattern — CAVEAT + CCM Framework

Detects cloud-specific threats using the CSA CAVEAT framework:
- 60+ cloud attack techniques with CSP-specific context
- AWS / Azure / GCP targeted mitigations and detection guidance
- MITRE ATT&CK cloud sub-technique mapping (T1xxx.00x)

Compliance layer via CSA CCM v4.1 → ATT&CK mapping (CTID):
- get_ccm_controls(threats) returns CCM control IDs for all detected T#### IDs
- Each CCM control includes SSRM layer (shared/csc/csp) and SG SSP prefix hint

Data sources:
  chatbot/data/caveat/caveat_techniques.yaml      — generated by fetch_caveat.py
  chatbot/data/caveat/caveat_mitre_mapping.yaml   — hand-authored MITRE bridge
  chatbot/data/ccm/ccm_by_technique.yaml          — generated by fetch_ccm.py

VERSION: 1.1 — Added CCM compliance enrichment
"""

import logging
from dataclasses import dataclass
from typing import Dict, List, Optional, Set

from chatbot.modules.pattern_registry import ThreatPattern
from chatbot.modules.caveat_helper import get_caveat_helper
from chatbot.modules.ccm_helper import get_ccm_helper

logger = logging.getLogger(__name__)


